	ORG	0000H
	SJMP	START

START:	
	CALL	INITDATA
	CALL	ADDNUM
	SJMP	$

ADDNUM:	
	MOV	R0, #30H
	CLR	A
	CLR	C
	MOV	R2, 10H
	LOOP1:	
		MOV	A, @R0
		JNB	ACC.7,POSNUM
		JB		ACC.7,NEGNUM

		POSNUM:
			ADD	A, 28H
			MOV	28H, A
			MOV	A, 29H
			ADDC	A, #0H
			MOV	29H, A
			INC	R0
			DJNZ	R2, LOOP1
			RET

		NEGNUM:
			ADD	A, 2AH
			MOV	2AH, A
			MOV	A, 2BH
			ADDC	A, #0H
			MOV	2BH, A
			INC	R0
			DJNZ	R2, LOOP1	
			RET
		
INITDATA: 
	MOV	30H, #-69H
	MOV	31H, #65H
	MOV	32H, #-7H
	MOV	33H, #-25H
	MOV	34H, #-47H
	MOV	35H, #56H
	MOV	36H, #-3H
	MOV	37H, #17H
	MOV	38H, #6H
	MOV	39H, #-5H
	MOV	3AH, #-45H
	MOV	3BH, #-1H
	MOV	3CH, #-57H
	MOV	3DH, #23H
	MOV	3EH, #-5H
	MOV	3FH, #-13H

	MOV	10H, #16
	RET

	END
