	ORG	0000H
	SJMP	START

START:	
	CALL	INITDATA
	CALL	CALCULA
	SJMP	$

CALCULA:	
	MOV	DPTR, #8100H
	MOV	R2, 10H
LOOP:					  
	MOVX	A, @DPTR
	JZ	ADDZERO
	JB	ACC.7, ADDNEGNUM
	JNB	ACC.7, ADDPOSNUM
		ADDZERO:
			ADD	A, 0
			MOV	DPTR, #8101
			MOVX	@DPTR, A
			RET
		ADDNEGNUM:
			CPL	A
			MOV	DPTR, #8101
			MOVX	@DPTR, A
			RET
		ADDPOSNUM:
			ADD	A, ACC
			MOV	DPTR, #8101H
			MOVX	@DPTR, A
			RET
						
INITDATA:
	MOV	DPTR, #8100H
   MOV	A, #-1h
	MOVX	@DPTR, A
	MOV	30H, #-69H
	MOV	31H, #65H
	MOV	32H, #-7H
	MOV	33H, #-25H
	MOV	34H, #-47H
	MOV	35H, #56H
	MOV	36H, #-3H
	MOV	37H, #0H
	MOV	38H, #6H
	MOV	39H, #-5H
	MOV	3AH, #0H
	MOV	3BH, #-1H
	MOV	3CH, #-57H
	MOV	3DH, #23H
	MOV	3EH, #-5H
	MOV	3FH, #-13H

	MOV	10H, #16
	RET
	
